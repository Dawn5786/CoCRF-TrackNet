import numpy as np
import os
from trackprocess.evaluation.data import Sequence, BaseDataset, SequenceList


def UAVPartDataset():
    return UAVPartDatasetClass().get_sequence_list()


class UAVPartDatasetClass(BaseDataset):
    """  dataset
    """
    def __init__(self):
        super().__init__()
        # self.base_path = self.env_settings.otb_path
        self.base_path = self.env_settings.uav_part_path
        self.sequence_info_list = self._get_sequence_info_list()

    def get_sequence_list(self):
        return SequenceList([self._construct_sequence(s) for s in self.sequence_info_list])

    def _construct_sequence(self, sequence_info):
        sequence_path = sequence_info['path']
        nz = sequence_info['nz']
        ext = sequence_info['ext']
        start_frame = sequence_info['startFrame']
        end_frame = sequence_info['endFrame']

        init_omit = 0
        if 'initOmit' in sequence_info:
            init_omit = sequence_info['initOmit']

        if isinstance(ext, list):
            for e in ext:
                first_frame_path = '{base_path}/{sequence_path}/{frame:0{nz}}.{ext}'.format(base_path=self.base_path,
                                                                                            sequence_path=sequence_path,
                                                                                            frame=start_frame + init_omit,
                                                                                            nz=nz,
                                                                                            ext=e)
                if os.path.isfile(first_frame_path):
                    ext = e
                    break

            if isinstance(ext, list):
                raise Exception('Sequence {} not found'.format(sequence_info['name']))

        frames = ['{base_path}/{sequence_path}/{frame:0{nz}}.{ext}'.format(base_path=self.base_path, 
        # sequence_path=sequence_path, frame=frame_num, nz=nz, ext=ext) for frame_num in range(start_frame+init_omit, end_frame+1)]
        sequence_path=sequence_path, frame=frame_num, nz=nz, ext=ext) for frame_num in range(start_frame, end_frame+1)]

        anno_path = '{}/{}'.format(self.base_path, sequence_info['anno_path'])

        try:
            ground_truth_rect = np.loadtxt(str(anno_path), dtype=np.float64)
        except:
            ground_truth_rect = np.loadtxt(str(anno_path), delimiter=',', dtype=np.float64)

        # return Sequence(sequence_info['name'], frames, ground_truth_rect[init_omit:,:])
        # return Sequence(sequence_info['name'], frames, ground_truth_rect)
        return Sequence(sequence_info['name'], frames, ground_truth_rect, sequence_info['initOmit'])


